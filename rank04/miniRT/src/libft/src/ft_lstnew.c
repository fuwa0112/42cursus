#include "libft.h"
#include <stdlib.h>

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = (t_list *) malloc(sizeof(t_list));
	if (!new)
		return ((void *) 0);
	new->content = content;
	new->next = (void *) 0;
	return (new);
}
